#define LBLK	"\033[0;30m"
#define LRED 	"\033[0;31m"
#define LGRN 	"\033[0;32m"
#define LYEL 	"\033[0;33m"
#define LBLU 	"\033[0;34m"
#define LMAG 	"\033[0;35m"
#define LCYN 	"\033[0;36m"
#define LWHT 	"\033[0;37m"

