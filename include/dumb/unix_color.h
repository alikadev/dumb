#define BGRN 	"\033[1;32m"
#define BYEL 	"\033[1;33m"
#define BBLU 	"\033[1;34m"
#define BMAG 	"\033[1;35m"
#define BCYN 	"\033[1;36m"
#define BWHT 	"\033[1;37m"

// set background
#define BLKB	"\033[40m"
#define REDB	"\033[41m"
#define GRNB 	"\033[42m"
#define YELB 	"\033[43m"
#define BLUB 	"\033[44m"
